package Practice;

//main 메서드가 있어서 컴퓨터에서 바로 실행할 수 있는 클래스
public class Main_1 {
	
	//실행되는 메서드
	public static void main(String[] args) {
		//3 + 4 = 7이라고 출력하기 (7은 3+4를 해서 출력)
		System.out.println(3 + "+" + 4 + "=" + (3+4));
		
		//1탭 2탭 3탭이라고 출력하기 : 탭 -> \t
		System.out.println("1\t2\t3\t");
		
		//c:\eclipse\ide를 출력하기
		System.out.println("c:\\eclipse\\ide");
		
		/*
		 Hello
		 World
		 */
		System.out.println("Hello\nWorld");
	}

}
